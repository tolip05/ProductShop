package org.productshop.domain.models.view;

public class CategoryViewModel {
    private String id;
    private String name;

    public CategoryViewModel() {
    }

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
